// Підказка: prompt завжди повертає рядок (String), тому перед перевіркою  
// перетворіть значення на число за допомогою Number().

let number = prompt("Ввести число");
number = Number(number);
if(number === 10){
    alert("Вірно");
} else {
    alert("Невірно");
}
