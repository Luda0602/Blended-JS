// const min = Math.floor(Math.random() * (59 - 0) + 0);
const min = Math.floor(Math.random() * 60);
let message = "";
if(min >= 0 && min < 15) {
    message = `${min} у першу чверть`;
} else if(min >= 15 && min < 30){
    message = `${min} у другу чверть`;
} else if (min >= 30 && min < 45 ) {
    message = `${min} у третю чверть`;
} else {
    message = `${min} у четверта чверть`; 
}